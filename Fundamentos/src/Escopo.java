
public class Escopo {

	public static void main(String[] args) {
		// No Java toda variável precisa ser iniciada
		// variável do tipo inteiro
		int x;
		boolean gameOver;
		// variável com valor
		x = 0;
		System.out.println(x);
		if (x == 0) {
			gameOver = false;
		} else {
			gameOver = true;
		}
		System.out.println(gameOver);
	}

}
